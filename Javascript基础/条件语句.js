/**
 * 条件语句是一种代码结构，用来测试表达式的真假，并根据测试结果运行不同的代码。
 * 一个常用的条件语句是if...else
 */
let iceCream = 'chocolate';
if (iceCream == 'chocolate') {
    alert('我最喜欢巧克力冰淇淋了。');
} else {
    alert('但是巧克力才是我的最爱呀...');
} // 我最喜欢巧克力冰淇淋了。
/**
 * if(...)中的表达式进行测试，用==运算符来比较变量iceCream与字符串'chocolate'是否相等。
 * 如果返回true，则运行第一个代码块；如果返回false，则跳过第一块直接运行else之后的第二个代码块。
 * 
*/