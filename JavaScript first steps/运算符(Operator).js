/**
 * 运算符 + 加 6 + 9
 * 运算符 - 减 20 - 15
 * 运算符 * 乘 3 * 7
 * 运算符 / 除 10 / 5
 * 可以使用 + 运算符将文本字符串连接在一起
 */

let name = 'Bingo';
name; // "Bingo"

let hello = ' says hello!';
hello; // " say hello!"

let greeting = name + hello;
greeting; // "Bingo says hello!"

/**
 * 比较运算符
 * === 严格等于(它们是否完全一样？) 
 * !== 不等于(它们究竟哪里不一样？)
 * < 小于 
 * > 大于
 */
5 === 2 + 4 // false
'Chris' !== 'Ch' + 'ris' // false
10 < 6 // false
10 > 20 // false